  <h2>Getting started</h2>

  <p>
    To get started with Nuklear, you'll need to include the CSS and JS files in
    your HTML. Here's how:
  </p>

  <ol>
    <li>
      Add the following line to the <code>head</code> of your HTML file to
      import the Nuklear CSS:

      <Code>&lt;link rel="stylesheet" href="https://maruchero.github.io/nuklear/v0.1.4/style/all.css"&gt;</Code>
    </li>

    <li>
      Add the following line to the <code>head</code> of your HTML file to
      import the Nuklear JS file:

      <Code>&lt;script src="https://maruchero.github.io/nuklear/v0.1.4/script/all.js"&gt;&lt;/script&gt;</Code>
    </li>
  </ol>

